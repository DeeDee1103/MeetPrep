#!/usr/bin/env node
/**
 * generate-api-docs.js
 *
 * Scans src/app/api/ for Next.js route handlers and auto-generates
 * docs/api-reference.md.
 *
 * Run: node scripts/generate-api-docs.js
 */

const fs   = require('fs')
const path = require('path')

const API_DIR = path.join(__dirname, '..', 'src', 'app', 'api')
const OUTPUT  = path.join(__dirname, '..', 'docs', 'api-reference.md')

const HTTP_METHODS = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE']

// Endpoints that do NOT require a user session
const PUBLIC_PATH_PREFIXES = [
  '/api/webhooks/',
  '/api/briefs/',   // public share reads
  '/api/auth/',
]

// ─── helpers ─────────────────────────────────────────────────────────────────

function findRouteFiles(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      findRouteFiles(full, files)
    } else if (entry.name === 'route.ts') {
      files.push(full)
    }
  }
  return files
}

function toApiPath(filePath) {
  return filePath
    .replace(API_DIR, '/api')
    .replace(/\\/g, '/')
    .replace('/route.ts', '')
    .replace(/\[([^\]]+)\]/g, '{$1}')
}

function extractMethods(content) {
  const found = []
  for (const method of HTTP_METHODS) {
    // Match: optional JSDoc block immediately before the exported function
    const regex = new RegExp(
      `(/\\*\\*[\\s\\S]*?\\*/)?\\s*export\\s+async\\s+function\\s+${method}\\s*\\(`,
      'g'
    )
    let match
    while ((match = regex.exec(content)) !== null) {
      let description = ''
      if (match[1]) {
        description = match[1]
          .replace(/\/\*\*|\*\//g, '')
          .replace(/^\s*\*\s?/gm, '')
          .trim()
          .split('\n')
          .find(l => l.trim() && !l.trim().startsWith('@'))
          ?.trim() ?? ''
      }
      found.push({ method, description })
    }
  }
  return found
}

function isPublic(apiPath) {
  return PUBLIC_PATH_PREFIXES.some(prefix => apiPath.startsWith(prefix))
}

// ─── generate markdown ────────────────────────────────────────────────────────

function generateMarkdown(routes) {
  const ts = new Date().toUTCString()

  const lines = [
    '# API Reference',
    '',
    '> **Auto-generated** by `scripts/generate-api-docs.js` — do not edit this file manually.',
    '> It is regenerated on every push that touches `src/app/api/**`.',
    '',
    `_Last updated: ${ts}_`,
    '',
    '---',
    '',
    '## Endpoints',
    '',
    '| Method | Endpoint | Auth Required | Description |',
    '|--------|----------|:-------------:|-------------|',
  ]

  for (const { apiPath, methods } of routes) {
    for (const { method, description } of methods) {
      const auth = isPublic(apiPath) ? 'No' : 'Yes'
      lines.push(`| \`${method}\` | \`${apiPath}\` | ${auth} | ${description || '—'} |`)
    }
  }

  lines.push(
    '',
    '---',
    '',
    '## Authentication',
    '',
    'All **protected** endpoints require a valid Supabase session (set via cookie on',
    'browser requests, or `Authorization: Bearer <token>` for server-to-server calls).',
    '',
    '**Webhook endpoints** use their own signature verification:',
    '',
    '| Webhook | Verification header |',
    '|---------|---------------------|',
    '| Google Calendar | `x-goog-channel-token` (matches userId stored at watch setup) |',
    '| Stripe | `stripe-signature` (HMAC using `STRIPE_WEBHOOK_SECRET`) |',
    '',
    '---',
    '',
    '## Error responses',
    '',
    'All endpoints return JSON. On error:',
    '',
    '```json',
    '{ "error": "Human-readable message" }',
    '```',
    '',
    '| Status | Meaning |',
    '|--------|---------|',
    '| `400` | Bad request / missing parameters |',
    '| `401` | Not authenticated |',
    '| `402` | Plan limit reached — upgrade required |',
    '| `404` | Resource not found |',
    '| `409` | Conflict (e.g. brief already generating) |',
    '| `500` | Internal server error |',
    '',
  )

  return lines.join('\n')
}

// ─── main ─────────────────────────────────────────────────────────────────────

const routeFiles = findRouteFiles(API_DIR)

const routes = routeFiles
  .map(file => ({
    apiPath: toApiPath(file),
    methods: extractMethods(fs.readFileSync(file, 'utf8')),
  }))
  .filter(r => r.methods.length > 0)
  .sort((a, b) => a.apiPath.localeCompare(b.apiPath))

fs.writeFileSync(OUTPUT, generateMarkdown(routes))
console.log(`✅  API reference written → ${OUTPUT}  (${routes.length} routes found)`)
